package com.microclos.uitl;

public class FileDownMessages {
	protected int messageNum;
	protected String fileName;
	protected String fileSize;
	protected String downStatus;
	protected String downSpeed;
	protected String downProgress;
	protected String downStartTime;
	protected String fileSavePath;

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getFileSize() {
		return fileSize;
	}

	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}

	public String getDownStatus() {
		return downStatus;
	}

	public void setDownStatus(String downStatus) {
		this.downStatus = downStatus;
	}

	public String getDownSpeed() {
		return downSpeed;
	}

	public void setDownSpeed(String downSpeed) {
		this.downSpeed = downSpeed;
	}

	public String getDownProgress() {
		return downProgress;
	}

	public void setDownProgress(String downProgress) {
		this.downProgress = downProgress;
	}

	public String getDownStartTime() {
		return downStartTime;
	}

	public void setDownStartTime(String downStartTime) {
		this.downStartTime = downStartTime;
	}

	public String getFileSavePath() {
		return fileSavePath;
	}

	public void setFileSavePath(String fileSavePath) {
		this.fileSavePath = fileSavePath;
	}
	
	

	public int getMessageNum() {
		return messageNum;
	}

	public void setMessageNum(int messageNum) {
		this.messageNum = messageNum;
	}

}
